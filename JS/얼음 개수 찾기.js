function solution(param) {
  const xlength = param[0].length;

  const ylength = param.length;
  function dfs(x, y) {
    if (x <= -1 || x >= xlength || y <= -1 || y >= ylength) {
      return false;
    }
    if (param[y][x] === 0) {
      param[y][x] = 1;
      //앞에꺼가 ture였으면 false로 바꿔버림
      dfs(x - 1, y);
      dfs(x, y - 1);
      dfs(x + 1, y);
      dfs(x, y + 1);
      return true;
    }
    return false;
  }

  let result = 0;
  for (let k = 0; k < ylength; k++) {
    for (let i = 0; i < xlength; i++) {
      if (dfs(i, k) === true) {
        result += 1;
      }
    }
  }
  return result;
}
